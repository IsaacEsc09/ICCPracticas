package carpeta;

public class OperadoresAritmeticos {

	public static void main (String args[]) {
		
		
		System.out.println("4+5 = " + (4+5) + " Resultado");
		System.out.println("10-4 = " + (10-4) + " Resultado");
		System.out.println("7x8 = " + (7*8) + " Resultado");
		System.out.println("12/2 = " + (12/2) + " Resultado");
		System.out.println("14%3 = " + (14%3) + " Resultado");
		
		System.out.println("5+3x2 = " + (5+3*2) + " Resultado");
		System.out.println("(5+3)x2 = " + ((5+3)*2) + " Resultado");
		
		System.out.println("(5+3)x(2+1) = " + ((5+3)*(2+1)) + " Resultado");
		System.out.println("5+3x2+1 = " + (5+3*2+1) + " Resultado");
		
		System.out.println("10/3 = " + (10.0/3.0) + " Resultado");
		
	}
	
}
